from __future__ import annotations

from datetime import date, datetime, timedelta, timezone
from typing import Literal

from pydantic import BaseModel, EmailStr, Field

from app.core.runtime import public_media_url


class User(BaseModel):
    id: str
    email: EmailStr
    display_name: str
    handle: str = "ty"
    bio: str = "Looking for clean waves, easy travel days, and people to paddle out with."
    surf_skill: Literal["", "beginner", "intermediate", "pro"] = "intermediate"
    avatar_url: str | None = None
    home_region: str
    locale: str
    premium: bool = False
    email_verified: bool = False
    free_live_spot_id: str | None = None
    ads_enabled: bool = True
    favorite_spot_ids: list[str] = Field(default_factory=list)


class Session(BaseModel):
    access_token: str
    token_type: Literal["bearer"] = "bearer"
    user: User


class Spot(BaseModel):
    id: str
    name: str
    country: str
    region: str
    area: str
    latitude: float
    longitude: float
    difficulty: Literal["beginner", "intermediate", "advanced"]
    best_months: list[str]
    wave_height_m: float
    water_temp_c: float
    image_url: str
    summary: str


class ForecastEntry(BaseModel):
    id: str
    spot_id: str
    day: date
    wave_height_m: float | None = None
    wave_height_min_m: float | None = None
    wave_height_max_m: float | None = None
    period_s: int | None = None
    wind_kts: float | None = None
    wind_kts_min: float | None = None
    wind_kts_max: float | None = None
    quality: Literal["poor", "fair", "good", "epic"]
    swell_wave_height_m: float | None = None
    wind_wave_height_m: float | None = None
    sea_surface_temperature_c: float | None = None
    source: str = "seed"
    confidence: Literal["live", "estimated"] = "estimated"
    confidence_note: str | None = None


class SurfWindowHour(BaseModel):
    time: str
    label: str
    wave_height_m: float
    period_s: int
    wind_kts: float
    score: float


class SurfWindowForecast(BaseModel):
    spot_id: str
    available: bool
    day: date | None = None
    best_start_label: str | None = None
    best_end_label: str | None = None
    rating: Literal["poor", "fair", "good", "epic"] = "poor"
    summary: str | None = None
    hours: list[SurfWindowHour] = Field(default_factory=list)
    source: str = "open-meteo"
    confidence: Literal["live", "estimated"] = "live"
    note: str | None = None


class TideEvent(BaseModel):
    type: Literal["high", "low"]
    time: datetime
    local_time: str
    local_date: date
    height_m: float | None = None


class TideForecast(BaseModel):
    spot_id: str
    available: bool
    station_name: str | None = None
    station_distance_km: float | None = None
    source: str = "unavailable"
    events: list[TideEvent] = Field(default_factory=list)
    note: str | None = None


class Trip(BaseModel):
    id: str
    user_id: str
    title: str
    destination: str
    start_date: date
    end_date: date
    travelers: int = 1
    notes: str = ""
    budget_usd: int = 0


class Alert(BaseModel):
    id: str
    user_id: str
    spot_id: str
    wave_enabled: bool = True
    min_wave_height_m: float | None = None
    wind_enabled: bool = True
    max_wind_kts: int | None = None
    tide_enabled: bool = False
    tide_type: Literal["high", "low"] | None = None
    tide_offset_hours: int | None = None
    enabled: bool = True
    status: Literal["watching", "triggered", "waiting"] = "watching"
    status_reason: str | None = None
    last_evaluated_at: datetime | None = None
    last_triggered_at: datetime | None = None
    next_check_at: datetime


class FriendProfile(BaseModel):
    id: str
    display_name: str
    home_region: str
    avatar_emoji: str
    vibe: str


class SocialMediaAttachment(BaseModel):
    id: str
    media_type: Literal["photo", "video"] = "photo"
    url: str
    thumbnail_url: str
    width: int | None = None
    height: int | None = None
    alt_text: str | None = None


class SocialPost(BaseModel):
    id: str
    user_id: str
    author_name: str
    author_handle: str | None = None
    author_avatar_url: str | None = None
    author_premium: bool = False
    spot_id: str | None = None
    post_type: Literal["looking_for_buddy", "surf_plan", "general"]
    visibility: Literal["public", "followers"] = "public"
    body: str
    media: list[SocialMediaAttachment] = Field(default_factory=list)
    meetup_date: date | None = None
    meetup_end_date: date | None = None
    created_at: datetime


class SocialComment(BaseModel):
    id: str
    post_id: str
    user_id: str
    author_name: str
    author_handle: str | None = None
    author_avatar_url: str | None = None
    author_premium: bool = False
    text: str
    reply_to_comment_id: str | None = None
    created_at: datetime


class SocialRepost(BaseModel):
    post_id: str
    created_at: datetime


class SocialEngagementState(BaseModel):
    liked_post_ids: list[str] = Field(default_factory=list)
    reposted_post_ids: list[str] = Field(default_factory=list)
    reposts: list[SocialRepost] = Field(default_factory=list)
    liked_comment_ids: list[str] = Field(default_factory=list)
    rsvp_post_ids: list[str] = Field(default_factory=list)
    comments: list[SocialComment] = Field(default_factory=list)


class BillingPlan(BaseModel):
    id: str
    name: str
    price_usd_monthly: float
    features: list[str]


class AdCard(BaseModel):
    id: str
    title: str
    partner: str
    cta: str
    image_url: str
    placement: Literal["home_feed", "spot_detail", "trip_planner"]


class Dashboard(BaseModel):
    featured_spot: Spot
    top_forecast: ForecastEntry
    upcoming_trip: Trip | None
    alerts_enabled: int


